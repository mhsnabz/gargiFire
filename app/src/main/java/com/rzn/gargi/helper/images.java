package com.rzn.gargi.helper;

public class images {
    String bir,iki,uc,dort,bes,alti;

    public images() {
    }

    public images(String bir, String iki, String uc, String dort, String bes, String alti) {
        this.bir = bir;
        this.iki = iki;
        this.uc = uc;
        this.dort = dort;
        this.bes = bes;
        this.alti = alti;
    }

    public String getBir() {
        return bir;
    }

    public String getIki() {
        return iki;
    }

    public String getUc() {
        return uc;
    }

    public String getDort() {
        return dort;
    }

    public String getBes() {
        return bes;
    }

    public String getAlti() {
        return alti;
    }
}
